/* Program 1: Write a dart program to check if a number is even or odd.

Input: var = 10;
Output: 10 is an even no
Input: var = 37;
Output: 37 is an odd no

Solution:
*/



void main() {
  var num = 10;
  
  if(num % 2 == 0){
    print("10 is an even no");
  } else {
    print("10 is an odd no");
  }
  
}

